"""
Модуль артефактов (достижений игры)

Артефакт — это коллекционная награда игрока,
которая выдаётся при выполнении особых условий
в сюжетных ветках и игровых сценариях.
"""


class Artifact:
    """
    Класс единичного артефакта (достижения)

    id   — программный идентификатор
    name — отображаемое название
    desc — описание условия получения
    """

    def __init__(self, id, name, desc):
        self.id = id
        self.name = name
        self.desc = desc


